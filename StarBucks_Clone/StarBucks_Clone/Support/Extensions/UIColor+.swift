import UIKit

extension UIColor {

  @nonobjc class var seaweedGreen: UIColor {
    return UIColor(red: 46.0 / 255.0, green: 153.0 / 255.0, blue: 95.0 / 255.0, alpha: 1.0)
  }

  @nonobjc class var brownGrey: UIColor {
    return UIColor(white: 126.0 / 255.0, alpha: 1.0)
  }

}
